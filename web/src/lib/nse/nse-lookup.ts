export async function searchNseCompanies(query: string, limit = 15): Promise<CompanyOption[]> {
  const q = query.trim().toUpperCase();
  if (!q) {
    return NSE_DIRECTORY.slice(0, limit);
  }

  const matches = NSE_DIRECTORY.filter(
    (comp) => comp.ticker.includes(q) || comp.name.toUpperCase().includes(q)
  );

  if (matches.length > 0) {
    return matches.slice(0, limit);
  }

  // If dynamic ticker not in directory, allow instant lookup format
  return [
    {
      ticker: q,
      name: `${q} (NSE / BSE India)`,
      market: "IN",
    },
  ];
}

export async function getNseCompanyBySymbol(symbol: string): Promise<CompanyOption | null> {
  const sym = symbol.trim().toUpperCase();
  const found = NSE_DIRECTORY.find((comp) => comp.ticker === sym);
  if (found) return found;

  return {
    ticker: sym,
    name: `${sym} (NSE / BSE India)`,
    market: "IN",
  };
}

